#!/usr/bin/env python3
from __future__ import annotations

import sys
from datetime import datetime, timezone
from pathlib import Path

SCRIPT_DIR = Path(__file__).resolve().parent
sys.path.insert(0, str(SCRIPT_DIR))
sys.path.insert(0, str(SCRIPT_DIR.parent))

from _loom_lib.core import find_workspace_root  # noqa: E402
from _loom_lib.memory import (
    collect_archive_rows,
    memory_root,
    validate_memory_structure,
)  # noqa: E402


def utc_today() -> str:
    return datetime.now(timezone.utc).strftime("%Y-%m-%d")


def render_index(rows: list[dict[str, str | int]]) -> str:
    lines = [
        "<!-- L0: Archive catalog for stored memory snapshots -->",
        "# Memory Glacier Index",
        "",
        "<!-- Auto-generated by rebuild_memory_glacier_index.py. Do not edit manually. -->",
        f"<!-- Last updated: {utc_today()} -->",
        "",
        "| File | Domain | Type | Tags | Date Range | Entries | Summary |",
        "|------|--------|------|------|------------|---------|---------|",
    ]
    if rows:
        for row in rows:
            lines.append(
                "| `{path}` | {domain} | {type} | {tags} | {date_range} | {entries} | {summary} |".format(
                    path=row["path"],
                    domain=row["domain"] or "-",
                    type=row["type"] or "-",
                    tags=row["tags"] or "-",
                    date_range=row["date_range"] or "-",
                    entries=row["entries"],
                    summary=row["summary"] or "-",
                )
            )
    else:
        lines.append("| - | - | - | - | - | 0 | No archived memory files yet. |")
    lines.append("")
    return "\n".join(lines)


def main() -> int:
    workspace = find_workspace_root()
    validate_memory_structure(workspace)
    output = memory_root(workspace) / "glacier/index.md"
    rows = collect_archive_rows(workspace)
    output.write_text(render_index(rows))
    print(output.relative_to(workspace))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
